import java.util.ArrayList;

public class CollectionArray {

	public static void main(String[] args) {
		ArrayList<Integer> Marks = new ArrayList<Integer>();
		Marks.add(001);
		Marks.add(002);
		Marks.add(003);

		System.out.println(Marks.size());
		System.out.println(Marks.get(0));
		System.out.println(Marks.get(1));
		System.out.println(Marks.get(2));
		ArrayList<Integer> Employee = new ArrayList<Integer>();

		Employee.add(11);
		Employee.add(13);
		Employee.add(14);

		System.out.println(Employee.size());
		System.out.println(Employee.get(0));
		System.out.println(Employee.get(1));
		System.out.println(Employee.get(2));
	

	}

}
